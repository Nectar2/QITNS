% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global KK;
global omiga;
global moz;
global VRF;
global rr;
global VAC;
global WAC;
        cla(handles.axes1,"reset"); 
        cla(handles.axes2,"reset");
        rr=str2double(get(handles.edit6,'String'))/1000;   %field radius
        P=str2double(get(handles.edit11,'String'));   %atmospheric pressure
        FRF=str2double(get(handles.edit10,'String'));   %RF frequency
        omiga=2*pi*FRF*1000;
        VRF=str2double(get(handles.edit1,'String'));   %RF voltage
        moz=str2double(get(handles.edit2,'String'));   %mass-to-charge ratio
        mop=28;    %Background gas mass number
        VAC=str2double(get(handles.edit9,'String'));   
        AA=1.76;   %polarization ratio
        FAC=str2double(get(handles.edit8,'String'));    %AC frequency
        WAC=2*pi*FAC*1000;
        KK=2*pi*P*(1.60217662E-19)/(moz*1.993*1E-26/12*omiga*1.3806505E-23*298.15)*sqrt(AA*1E-30*moz*mop*1.993*1E-26/12/(moz+mop)/(8.854187817E-12));
        set(handles.edit16,'String',KK);  
        Tfs=0.1;  %Time-frequency unit of simulation (us)
        Ttest=[0:Tfs:1000]/(1e6);   %calculation time
        Odata=[0.1*rr 0];    %Initial position and speed
        val=get(handles.popupmenu2,'Value');%Mode of excitation
        if val==1
            [T,F] = ode113(@diopolar,Ttest,Odata);
        end
        if val==2
            [T,F] = ode113(@quadrupolar,Ttest,Odata);
        end
        Tout=T*1E6;
        Rout=F(:,1)/rr;
        plot(Tout,Rout);
        axis([min(Tout) max(Tout) min(Rout) max(Rout)]); 
        axes(handles.axes1);
        plot(handles.axes1,Tout,Rout);
        xlabel('us')
        ylabel('mm')
        axis([min(Tout) max(Tout) min(Rout) max(Rout)]); 
        N1=length(Tout);
        fft1=abs(fft(Rout));
        FFT1=fft1/max(fft1);
        w1=(0:1000/Tfs/N1:(N1-1)/N1*1000/Tfs);  %unit(kHz)
        axes(handles.axes2);
        plot(handles.axes2,w1,FFT1);
        xlabel('kHz')
        %axis([0 FRF/2 0 1]);
        h=1;
guidata(hObject,handles);



